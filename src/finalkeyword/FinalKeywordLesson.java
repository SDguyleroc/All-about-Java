package finalkeyword;
// final - can be used on a variable, method, and class
public class FinalKeywordLesson {
    public static void main(String[] args)
    {
        // mutable varibale
        int num = 8;
        num=9;
        System.out.println(num);

        // final variable is constant it can't be changed once assigned
        final int num1 = 8;
        // num1=9; will get an error, because you can't assign a value to a final variable

        // Pie if an exmaple of a variable you might want to make final 3.14



    }
}
